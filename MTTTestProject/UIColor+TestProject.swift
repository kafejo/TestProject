import UIKit

extension UIColor {
    class func tstGreenColor() -> UIColor {
        return UIColor(red: 0.133, green: 0.812, blue: 0.553, alpha: 1.00)
    }
    
    class func tstGrayColor() -> UIColor {
        return UIColor(red: 0.635, green: 0.635, blue: 0.635, alpha: 1.00)
    }
}
